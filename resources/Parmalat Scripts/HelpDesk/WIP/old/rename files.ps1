Import-Module activedirectory
$outDir = 'WINDOWS\appdata\Microsoft\Outlook'
$Users = Get-Content -Path $users

ForEach ($user in $Users){
	$user = Get-ADUser -Identity $user
	$DN = 'LDAP://' + $user.distinguishedname
	$user = [ADSI]"$DN"
	[string]$path = $user.homeDirectory
	$path = $path + '\'
	$path = $path + $outDir
	$path = $path + '\*.nk2'
	$files = Get-ChildItem $path
	Foreach ($file in $files) {Rename-Item -Path $file.FullName -NewName ('__' + $file.name)}
}
